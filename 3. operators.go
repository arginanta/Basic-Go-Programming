package main

import "fmt"

// Contoh operator matematika dan perbandingan
func main() {
	a, b := 10, 3

	// Operator matematika
	fmt.Println("Addition:", a+b)
	fmt.Println("Subtraction:", a-b)
	fmt.Println("Multiplication:", a*b)
	fmt.Println("Division:", a/b)
	fmt.Println("Modulus:", a%b)

	// Operator perbandingan
	fmt.Println("a > b:", a > b)
	fmt.Println("a == b:", a == b)
}
